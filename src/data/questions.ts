const allCategories = Object.keys(questionsDB) as Category[]

/**
 * Returns `count` random categories, excluding those in `exclude`
 */
export function getRandomCategories(count: number, exclude: Category[] = []): Category[] {
  const available = allCategories.filter(c => !exclude.includes(c))
  const shuffled = [...available].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, shuffled.length))
}

/**
 * Returns a question for a given category and difficulty (1â10)
 */
export function getQuestion(category: Category, difficulty: number): Question {
  const questions = questionsDB[category]
  const match = questions.find(q => q.difficulty === difficulty)
  if (match) return match
  // Fallback: return closest difficulty
  const sorted = [...questions].sort((a, b) => Math.abs(a.difficulty - difficulty) - Math.abs(b.difficulty - difficulty))
  return sorted[0]
}
